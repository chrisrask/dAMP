int countGain(TIM_HandleTypeDef *htim){
	static unsigned long lastInterruptTime = 0;
	unsigned long interruptTime = HAL_GetTick();

	if (interruptTime - lastInterruptTime > 10){
		count1 = __HAL_TIM_GET_COUNTER(htim);
		if (count1 > count2){

			counterGain++;
		}
		else if (count1 < count2){
			counterGain--;
		}
	}
	if (counterGain >= 10){
		counterGain = 10;
	}
	else if (counterGain <= 2){
		counterGain = 2;
	}

	cGain = counterGain / 2;
	//switch
	switch(cGain){
		case 2:
			cGain = 3;
			break;
		case 3:
			cGain = 5;
			break;
		case 4:
			cGain = 7;
			break;
		case 5:
			cGain = 11;
			break;
		default:
			cGain = 1;
			break;
		}
	count2 = count1;
	lastInterruptTime = interruptTime;
	return cGain;
}

float countImpedance(TIM_HandleTypeDef *htim){

	static unsigned long lastInterruptTime = 0;
	unsigned long interruptTime = HAL_GetTick();

	if (interruptTime - lastInterruptTime > 10){
		count1 = __HAL_TIM_GET_COUNTER(htim);
		if (count1 > count2){
			counterImp++;
		}
		else if (count1 < count2){
			counterImp--;
		}
		if (counterImp > 4){
			counterImp = 4;
		}
		else if (counterImp <= 0){
			counterImp = 0;
		}
	}

	i = counterImp / 2;
	cImpedance = (float)i / 2;
	aLastState = aState;
	count2 = count1;
	lastInterruptTime = interruptTime;
	return cImpedance;
}


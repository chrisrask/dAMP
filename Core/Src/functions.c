			HAL_GPIO_WritePin(mux1_GPIO_Port, mux1_Pin, GPIO_PIN_RESET);
			HAL_GPIO_WritePin(mux2_GPIO_Port, mux2_Pin, GPIO_PIN_SET);
			HAL_GPIO_WritePin(mux3_GPIO_Port, mux3_Pin, GPIO_PIN_RESET);

			relay = 11;
			HAL_GPIO_WritePin(opRelay_GPIO_Port, opRelay_Pin, GPIO_PIN_RESET);
			HAL_GPIO_WritePin(mux1_GPIO_Port, mux1_Pin, GPIO_PIN_RESET);
